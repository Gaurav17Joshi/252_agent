"""
stage2/blender_exporter.py

Blender Script Exporter
━━━━━━━━━━━━━━━━━━━━━━
Generates a self-contained Python script (``simulation.py``) that can be
run inside Blender to set up and execute the physics simulation.

The script:
  1. Clears the default Blender scene.
  2. Imports each object mesh (.obj) into Blender.
  3. Assigns the correct physics type (Rigid Body / Fluid / Cloth / Soft Body).
  4. Sets numerical physics parameters from the material map.
  5. Creates force fields for each inferred force.
  6. Configures the scene timeline and bakes the physics simulation.
  7. Renders the animation (optional).

No Claude tool-use loop is needed here — the exporter is deterministic
given the structured inputs from Stages 1 and 2.
"""

